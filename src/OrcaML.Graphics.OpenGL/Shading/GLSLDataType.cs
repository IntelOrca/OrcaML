namespace OrcaML.Graphics.OpenGL.Shading
{
    public enum GLSLDataType
    {
        // Layout (t << 4) | (n << 2) | (m << 0)
        Bool    = (0 << 4) | (0 << 2) | (0 << 0),
        Bvec2   = (0 << 4) | (0 << 2) | (1 << 0),
        Bvec3   = (0 << 4) | (0 << 2) | (2 << 0),
        Bvec4   = (0 << 4) | (0 << 2) | (3 << 0),

        Int     = (1 << 4) | (0 << 2) | (0 << 0),
        Ivec2   = (1 << 4) | (0 << 2) | (1 << 0),
        Ivec3   = (1 << 4) | (0 << 2) | (2 << 0),
        Ivec4   = (1 << 4) | (0 << 2) | (3 << 0),

        Uint    = (2 << 4) | (0 << 2) | (0 << 0),
        Uvec2   = (2 << 4) | (0 << 2) | (1 << 0),
        Uvec3   = (2 << 4) | (0 << 2) | (2 << 0),
        Uvec4   = (2 << 4) | (0 << 2) | (3 << 0),

        Float   = (3 << 4) | (0 << 2) | (0 << 0),
        Vec2    = (3 << 4) | (0 << 2) | (1 << 0),
        Vec3    = (3 << 4) | (0 << 2) | (2 << 0),
        Vec4    = (3 << 4) | (0 << 2) | (3 << 0),

        Mat2x2  = (3 << 4) | (1 << 2) | (1 << 0),
        Mat2x3  = (3 << 4) | (1 << 2) | (2 << 0),
        Mat2x4  = (3 << 4) | (1 << 2) | (3 << 0),
        Mat3x2  = (3 << 4) | (2 << 2) | (1 << 0),
        Mat3x3  = (3 << 4) | (2 << 2) | (2 << 0),
        Mat3x4  = (3 << 4) | (2 << 2) | (3 << 0),
        Mat4x2  = (3 << 4) | (3 << 2) | (1 << 0),
        Mat4x3  = (3 << 4) | (3 << 2) | (2 << 0),
        Mat4x4  = (3 << 4) | (3 << 2) | (3 << 0),

        Double  = (4 << 4) | (0 << 2) | (0 << 0),
        Dvec2   = (4 << 4) | (0 << 2) | (1 << 0),
        Dvec3   = (4 << 4) | (0 << 2) | (2 << 0),
        Dvec4   = (4 << 4) | (0 << 2) | (3 << 0),

        Dmat2x2 = (4 << 4) | (1 << 2) | (1 << 0),
        Dmat2x3 = (4 << 4) | (1 << 2) | (2 << 0),
        Dmat2x4 = (4 << 4) | (1 << 2) | (3 << 0),
        Dmat3x2 = (4 << 4) | (2 << 2) | (1 << 0),
        Dmat3x3 = (4 << 4) | (2 << 2) | (2 << 0),
        Dmat3x4 = (4 << 4) | (2 << 2) | (3 << 0),
        Dmat4x2 = (4 << 4) | (3 << 2) | (1 << 0),
        Dmat4x3 = (4 << 4) | (3 << 2) | (2 << 0),
        Dmat4x4 = (4 << 4) | (3 << 2) | (3 << 0),

        Sampler = (5 << 4) | (0 << 2) | (0 << 0),

        Void    = (6 << 4) | (0 << 0) | (0 << 0),
    }
}
